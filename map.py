class Map:
    def __init__(self, game):
        self.game = game
        self.game_map = {}
        self.current_zone = 0
        self.start_block = 3, 33
        self.wall = (1, 2, 3, 4, 5, 6)
        self.wall_color = {
            1: [0, 0, 255],
            2: [255, 255, 255],
            3: [181, 101, 29],
            4: [80, 200, 120],
            5: [141, 145, 141],
            6: [255, 255, 0]
        }
        self.get_map()

    def get_map(self):
        for y, row in enumerate(game_map):
            for x, block in enumerate(row):
                self.game_map[(x, y)] = block

    def draw(self):
        for pos in self.game_map:
            if self.game_map[pos] in self.wall:
                pg.draw.rect(self.game.screen, 'white',
                             (pos[0] * scale_2d, pos[1] * scale_2d, scale_2d, scale_2d))
